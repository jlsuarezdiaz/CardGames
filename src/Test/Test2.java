    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FrenchCard c = deck.nextCard();
        if(c != null){
            
                      

            VerticalHand.add(c,FrenchCardBack.BLUE);
            VerticalHand.repaint();
            VerticalHand.revalidate();
        }
        else{
            JOptionPane.showMessageDialog(this, "No quedan cartas", "No quedan cartas", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

