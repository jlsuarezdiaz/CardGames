    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        ArrayList<Card> covered = HorizontalHand.getCoveredCards();
        for(Card c : covered){
            deck.giveCardBack(c);
        }
        HorizontalHand.remove(covered);
        HorizontalHand.repaint();
        HorizontalHand.revalidate();
    }//GEN-LAST:event_jButton7ActionPerformed

