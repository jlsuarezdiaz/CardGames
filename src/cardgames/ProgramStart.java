    public void showView(int ms){
        this.setVisible(true);
        if(ms > 0){
            timeBar.setMaximum(ms);
            timeBar.setMinimum(0);
            int i = 0;
            while(i <= ms){
                i++;
                try {
                    sleep(1);
                } catch (InterruptedException ex) {}
                timeBar.setValue(i);
            }
        }
        this.dispose();
    }

